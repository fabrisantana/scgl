package br.com.SCGLapi.security;

import br.com.SCGLapi.repository.UsuarioRepository;

import javax.transaction.Transactional;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Repository;

import br.com.SCGLapi.model.Usuario;


@Repository
@Transactional
public class ImplementsUserDetailsService implements UserDetailsService{

	@Autowired
	private UsuarioRepository ur;
	
	@Override
	public UserDetails loadUserByUsername(String loginUsuario) throws UsernameNotFoundException {
		Usuario usuario = ur.findByLogin(loginUsuario);
		
		if(usuario == null || !usuario.isEnabled()){
			throw new UsernameNotFoundException("Usuario n√£o encontrado!");
		}
		return new User(usuario.getUsername(), usuario.getPassword(), true, true, true, true, usuario.getAuthorities());
	}
	
	
}
